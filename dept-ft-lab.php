	<p>Department of Food Technology is having state of the art lab facilities for effective testing of food samples to conform to legal standards. All the labs in the department are exclusively available for quality evaluation of foods with advanced instruments. Some of the Equipment available in Department of Food Technology include UV Spectrophotometer, Gerber Centrifuge, Brookfield Viscometer, Pin mill, Burr mill, Hammer mill, Rotary flash evaporator, Laminar air flow, Fermentation cabinet, Tray dryer, Plate heat exchanger, Pasteurise, Soxhlet and Kjeldhal apparatus etc., </p>
