	<h6 class="page-title">List of Equipment: </h6>
	<p>Pasteurizer, Vegetable Chopper, Mixer, Plate Heat Exchanger, Deep Freezer, Tray Dryer, Hot Air Oven, Hand Refractometer, Extruder, Retort Unit, Water Vapour Transmission Tester, Viscometer</p>
