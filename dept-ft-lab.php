	<p>Sedimentation Cylinder, Deck Oven, Dough Mixer, Fermentation Cabinet, Humidity Chamber, Hunter Colorimeter, Texture Analyzer, Refrigerator, Extruder, Oven Toaster Grill (OTG) Oven, Electric Blender</p>

