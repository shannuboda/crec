                        <li><strong>Problem Analysis:</strong> Identify, formulate, research literature and analyze
                            complex engineering problems reaching substantiated conclusions using first principles of
                            mathematics, natural sciences & engineering sciences.</li>
                        <li><strong>Design/Development of Solutions:</strong> Design solutions for complex engineering
