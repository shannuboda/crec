                    <p>
                        The Second Shift Polytechnic at Chadalawada Ramanamma Engineering College (Autonomous),
                        Chadalawada Nagar, Renigunta Road, Tirupati aims to offer diploma programs to students that pave
                        the way for their enhanced professional education. Initially, the second shift polytechnic was
                        humbly commenced its journey in the year 2013 and associated with the Chadalawada Venkata
                        Subbaiah College of Engineering, Chadalawada Nagar, Renigunta Road, Tirupati. In the academic
                        year 2013-2014, it commenced with two diploma programs viz. Diploma in Civil Engineering
                        (D.C.E.) and Diploma in Electronics and Communication Engineering (D.E.C.E.). In the academic
                        year 2022-2023, in view of merging process, the second shift polytechnic was associated with
                        Chadalawada Ramanamma Engineering College (Autonomous), Chadalawada Nagar, Renigunta Road,
                        Tirupati. In the academic year 2023-2024, the second shift polytechnic is now offering four
                        diploma programs viz. Diploma in Civil Engineering (D.C.E.), Diploma in Computer Engineering
                        (D.C.M.E.), Diploma in Electrical and Electronics Engineering (D.E.E.E.) and Diploma in
                        Electronics and Communication Engineering (D.E.C.E.).
                        <br><br>
                        The polytechnic basically offers diploma programs which include hands-on practical education and
                        training in diversified technical fields. The diploma programs are offered to candidates who
                        successfully completed their 10th standard. These programs provide knowledge and training to the
                        participants which help them to pursue career in various fields of engineering. The duration of
                        the course is three years and offers immense practical exposure to the candidates. After
                        successfully completing the diploma programs, the students are able to join directly into second
                        year B.Tech. programs through lateral entry admission process. Besides this, the students who
                        successfully completed their diploma programs can also apply for the AMIE Certification Program
                        offered by the Associate Member of the Institutions of Engineers. Many public and private sector
                        companies actively employ candidates holding a diploma as these candidates are known to have
                        relevant hands-on skills and industry-relevant knowledge to excel at their job.
                        <br><br>
                        The second shift polytechnic at Chadalawada Ramanamma Engineering College (Autonomous),
                        Chadalawada Nagar, Renigunta Road, Tirupati is committed to offer quality education blended with
                        hands-on practical education on advanced laboratories. The highly qualified and well-experienced
                        faculty members disseminate their knowledge to students in building strong skills in diversified
                        areas. The standard practices make our students to be confident in handling challenges and
                        paving their bright future path.
