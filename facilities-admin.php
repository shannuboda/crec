                    <h1 class="page-title">Water Conservation</h1>
                    <p>

                        Our college has very good water management plan to support all our water needs including both
                        ladies and gent’s hostel. The water management is well supported with rain water harvesting and
                        sump. The ground water is circulated to all overhead tanks using borewells. The college also has
                        a sewage treatment plant to do water recycling and use it for watering plants and to support
                        green energy.<br><br>

                        The rainwater is harvested to charge the rain water into the soil for ground water recharging.
                        The storage of rainwater on surface is a traditional techniques and structures used were
                        underground tanks, ponds, check dams, weirs etc. Due to certain difficulties on going ahead with
                        traditional techniques the campus has installed 16 to 18 numbers of ground water recharging pits
                        to recharge storm water/rainwater to maximum extent.<br><br>

                        The borewells are constructed in water abundant points inside and outside college. The ground
                        water was pumped to sump using borewell. There are totally 8 sumps inside the college to support
                        water necessities to cover all blocks of the college including hostel. The sump and borewell are
                        periodically monitored and maintained by plumbers and civil maintenance people to take immediate
                        actions. The sump water is loaded to overhead tanks. <br><br>

                        From the sump water is collected in overhead tank constructed at the top of each block. All
                        blocks have overhead tanks to support water necessities. The water from overhead tank is
                        separately treated by RO plant for drinking purpose. The drinking water is supplied in all
                        blocks. A well-arranged pipe network connecting sumps and overhead tank to the water
                        distributing point is established to support all water needs of the college.<br><br>

                        The institution always has a motto in saving water for future. In this regard a Wastewater
                        treatment plant of capacity 300KLD is available at the campus with present utilization value of
                        about 2,10,000 litres per day. The technology that has been adopted this WWTP is FMBR (Floating
                        Media Bioreactor) technology for the treatment of domestic sewage and other waste waters. This
                        technology ensures that the quality of the treated water confirms to the standards and
                        requirements as laid out by Central/State pollution control board and the same has been reviewed
                        and approved by NABL authorized laboratory on periodic basis to ensure on the quality. The water
                        recycled was used to gardening (70%), flushing toilets in Academic block (10%) and other
                        custodial purposes like hardscape cleaning, washing etc. (30%).</p>


