                    <h2 class="page-title">Bio Gas Plant</h2>
                    <p>
                        In REC, kitchen waste is used to generate thermal energy for cooking and heating.The bio-gas
                        produced from food waste, decomposable organic material and kitchen waste, consisting of methane
                        and a little amount of carbon dioxide is an alternative fuel for cooking gas (LPG). The bio gas
                        plant is set up to operate for 50 KG waste.<br><br>
                        Kitchen waste is processed and moistened to produce a suspension that subsequently undergoes a
                        fermentation process. Fermentation produces biogas – a valuable energy source – that is
                        desulphurised by biological means. Also, the waste materials can be disposed off efficiently
                        without any odour or flies and the digested slurry from the bio-gas unit can be used as organic
                        manure in the garden. The major components of the bio-gas plant are a digester tank, an inlet
                        for feeding the kitchen waste, gas holder tank, an outlet for the digested slurry and the gas
                        delivery system for taking out and utilizing the produced gas.</p>


                    <h2 class="page-title">Sensor based Energy conservation</h2>
                    In REC Sensors are used to control lights and water flow which helps to do energy conservation.
                    Sensor Based Solar Lights are installed in the college premises.<br><br>

                    <Strong>Water Flow Sensor</strong><br>
                    To monitor the amount of water being supplied and used, the rate of flow of water has to be
                    measured. Water flow sensors are used for this purpose. Water flow sensors are installed at the
                    water source or pipes in the library block, to measure the rate of flow of water and calculate the
                    amount of water flowing through the pipe. Rate of flow of water is measured as litres per hour or
                    cubic meters. <br><br>

                    <strong>PV Sourced Street Focus Lighting Systems</strong><br>
                    A photovoltaic-sourced street lighting system is most commonly promoted nowadays for illuminating a
                    street or an open area of rural and urban places. This system improves the efficient utilization of
                    energy consumption with the addition of suitable sensing and control devices. The main components of
                    the above system are the PV panel, Battery, Sensor, DC-DC converter, power electronics Inverter, and
                    real-time Timer. Here we prose a day-light sensor that opens the circuit electrically during the
                    sunlight's visible time period and closes the electrical circuit. The battery is used to store the
                    energy obtained from the Pohotovaltic array during the high sunshine time period and a suitable DC
                    to DC converter is used for charging the energy<br><br>

                    <strong>Wheeling to the Grid</strong><br>
                    A grid-connected photovoltaic system is an electricity generating solar PV power system that is
                    connected to the utility building load. A grid-connected PV system consists of solar panels, one
                    inverter, DC/AC isolator switch, a power conditioning unit and grid connection equipment.<br><br>

                    <strong>Operation of our system works:-</strong><br>
                    There are two grid connected system, one is solar PV system has connected Energy shortfall- internal
                    demand is more than solar energy produced is drawn from the grid and another one is PV system is
                    Energy surplus/excess power (solar energy produced is more than internal demand) is fed into the
                    grid-Power exported to the grid. <br><br>

